package ss4_class_oob_java.exc.bai_tap_stop_watch;

public class StopWatch {
    private double startTime;
    private double endTime;

    public StopWatch() {

    }

    public double getStartTime() {
        return startTime;
    }

    public double getEndTime() {
        return endTime;
    }

    public double start(){
        return startTime;
    }

    public double stop(){
        return endTime;
    }

}
